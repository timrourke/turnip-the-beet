        <div class="blog-post__content blog-post__content--excerpt">
          <p>Hey Everyone,</p>
          <p>Sorry it’s been awhile since our last letter. Things have sure been busy here. As you know, the official first day of spring happened last Friday … but spring truly came early for us this season judging by the weather. Unbelievable. A sustained stretch of dry and sunny in February and March situated us in a stronger than usual starting position. Shared feelings of luck and confusion … this is the season and we have to act.</p>
          <p>Springtime is always wonderful and precarious. A season that carries us between extremes, it is charged with potential and shaky predictions. In a “normal” season it can be a period of casting ourselves into the unknown: we schedule direct seeding and transplants dates hoping without certainty that the ground will come available – that our seeding dates will line up just so with a long enough break in the weather to mow down cover crop, work up soil, incorporate compost and amendments, and do our final bed preparations. Not so this season…</p>
          <p>This year, catching an exceptionally lengthy dry stretch early on, we were able to do all of our early field work at once. What this means is, even as the rains return, we are better prepared than ever. The ground, ready and waiting, promises security, flow, and a rare organization to the months ahead of us. Less tractor time, more boots on the ground. Full steam ahead!</p>
          <p>But it’s not as if we don’t have our work cut out for us. Early heat means early insects, and already we’re seeing record breaking numbers of cucumber beetles in the fields. As to just what the weather will do over the course of spring – no one can say for sure. Persistence is the name of the game.</p>
          <p>So much going on and getting done. Here are some highlights:</p>
          <ul>
            <li>First round of tomatoes, peppers, eggplants sizing up well for their destiny in the greenhouse, ready for transplant in just a couple of weeks</li>
            <li>First round of basil scheduled to go in the ground next week</li>
            <li>800 bed feet of peas already grabbing trellis</li>
            <li>Succession planting of bok chois, lettuces, and scallions</li>
            <li>Flowering fava beans</li>
            <li>Nettles are on</li>
            <li>Kale going to raab</li>
            <li>Overflowing propagation house</li>
            <li>Spicy mix and French breakfast radishes harvest beginning</li>
            <li>2,000 bed feet of potatoes</li>
            <li>20,000 onions planted</li>
            <li>Cucumbers, melons, zucchinis just sprouting</li>
          </ul>
          <p>So much going on every day and so much more ahead of us. It feels great to be out here doing it remembering the essence of why we do what we do: to provide you and yours with the purest food possible. Special thanks to our CSA members, without y’all we would not have the upfront capital to make the necessary investments to get this season going. Your early support enabled us to make the most of this early spring and we cannot thank you enough.</p>
          <p>Thanks everyone for making local food a priority and investing directly in small scale, sustainable agriculture.</p>
          <p>Hope you’re all enjoying this time of year as much as we are. Lots going on, lots to look forward to.</p>
          <p>Onward,</p>
