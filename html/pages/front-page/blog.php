          <h3 class="post-title">Happy summer!</h3>
          <div class="meta-row">
            <span class="post-date">March 22, 2015</span>
            <span class="sep">&nbsp;~&nbsp;</span>
            <span class="post-weather">58&#176;F and sunny</span>
